import { verifySudokuBoard } from "../../2-hashmaps-and-sets/2-verify-sudoku-board";

describe("Verify Sudoku Board", () => {
  it("returns true for a valid completed board", () => {
    const board = [
      [5, 3, 4, 6, 7, 8, 9, 1, 2],
      [6, 7, 2, 1, 9, 5, 3, 4, 8],
      [1, 9, 8, 3, 4, 2, 5, 6, 7],
      [8, 5, 9, 7, 6, 1, 4, 2, 3],
      [4, 2, 6, 8, 5, 3, 7, 9, 1],
      [7, 1, 3, 9, 2, 4, 8, 5, 6],
      [9, 6, 1, 5, 3, 7, 2, 8, 4],
      [2, 8, 7, 4, 1, 9, 6, 3, 5],
      [3, 4, 5, 2, 8, 6, 1, 7, 9],
    ];
    expect(verifySudokuBoard(board)).toBeTruthy();
  });
  it("returns false for a board with duplicate in a row", () => {
    const board = [
      [5, 3, 5, 0, 7, 0, 0, 0, 0], // 5 repeated
      [6, 0, 0, 1, 9, 5, 0, 0, 0],
      [0, 9, 8, 0, 0, 0, 0, 6, 0],
      [8, 0, 0, 0, 6, 0, 0, 0, 3],
      [4, 0, 0, 8, 0, 3, 0, 0, 1],
      [7, 0, 0, 0, 2, 0, 0, 0, 6],
      [0, 6, 0, 0, 0, 0, 2, 8, 0],
      [0, 0, 0, 4, 1, 9, 0, 0, 5],
      [0, 0, 0, 0, 8, 0, 0, 7, 9],
    ];
    expect(verifySudokuBoard(board)).toBeFalsy();
  });
  it("returns false for a board with duplicate in a column", () => {
    const board = [
      [5, 3, 0, 0, 7, 0, 0, 0, 0],
      [5, 0, 0, 1, 9, 5, 0, 0, 0], // 5 repeated in column 0
      [0, 9, 8, 0, 0, 0, 0, 6, 0],
      [8, 0, 0, 0, 6, 0, 0, 0, 3],
      [4, 0, 0, 8, 0, 3, 0, 0, 1],
      [7, 0, 0, 0, 2, 0, 0, 0, 6],
      [0, 6, 0, 0, 0, 0, 2, 8, 0],
      [0, 0, 0, 4, 1, 9, 0, 0, 5],
      [0, 0, 0, 0, 8, 0, 0, 7, 9],
    ];
    expect(verifySudokuBoard(board)).toBeFalsy();
  });
  it("returns false for a board with duplicate in a subgrid", () => {
    const board = [
      [5, 3, 0, 0, 7, 0, 0, 0, 0],
      [6, 5, 0, 1, 9, 5, 0, 0, 0], // 5 repeated in top-left subgrid
      [0, 9, 8, 0, 0, 0, 0, 6, 0],
      [8, 0, 0, 0, 6, 0, 0, 0, 3],
      [4, 0, 0, 8, 0, 3, 0, 0, 1],
      [7, 0, 0, 0, 2, 0, 0, 0, 6],
      [0, 6, 0, 0, 0, 0, 2, 8, 0],
      [0, 0, 0, 4, 1, 9, 0, 0, 5],
      [0, 0, 0, 0, 8, 0, 0, 7, 9]
    ];
    expect(verifySudokuBoard(board)).toBeFalsy();
  });
  it("returns true for an empty board", ()=>{
    const board = Array.from({ length: 9 }, () => Array(9).fill(0));
    expect(verifySudokuBoard(board)).toBeTruthy();
  });
  it("returns true for a valid partially filled board", () => {
    const board = [
      [5, 3, 0, 0, 7, 0, 0, 0, 0],
      [6, 0, 0, 1, 9, 5, 0, 0, 0],
      [0, 9, 8, 0, 0, 0, 0, 6, 0],
      [8, 0, 0, 0, 6, 0, 0, 0, 3],
      [4, 0, 0, 8, 0, 3, 0, 0, 1],
      [7, 0, 0, 0, 2, 0, 0, 0, 6],
      [0, 6, 0, 0, 0, 0, 2, 8, 0],
      [0, 0, 0, 4, 1, 9, 0, 0, 5],
      [0, 0, 0, 0, 8, 0, 0, 7, 9]
    ];
    expect(verifySudokuBoard(board)).toBeTruthy();
  });
});
